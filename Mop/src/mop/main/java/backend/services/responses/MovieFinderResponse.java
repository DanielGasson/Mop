package mop.main.java.backend.services.responses;

public class MovieFinderResponse {

    private String title, director, plot, year, runtime, genre;

    public String getTitle() {

        return title;
    }

    public String getDirector() {

        return director;
    }

    public String getDescription() {

        return plot;
    }

    public String getYear() {

        return year;
    }

    public String getRuntime() {

        return runtime;
    }

    public String getGenre() {

        return genre;
    }
}
